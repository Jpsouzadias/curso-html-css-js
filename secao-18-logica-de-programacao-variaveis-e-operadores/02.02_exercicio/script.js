//pedir para usuário digitar um número
var n1 = prompt("Digite um número");
//converter em número inteiro
n1 = parseInt(n1);

//pedir para usuário digitar outro número
var n2 = prompt("Digite outro número");
//converter em número inteiro
n2 = parseInt(n2);

//mostrar a subtração concatenando com o texto "A subtração é: "
alert("A subtração é: " + (n1 - n2));
//mostrar a subtração concatenando com o texto "A multiplicação é: "
alert("A multiplicação é: " + (n1 * n2));
//mostrar a subtração concatenando com o texto "A divisão é: "
alert("A divisão é: " + (n1 / n2));
//mostrar a subtração concatenando com o texto "A soma é: "  
alert("A soma é: " + (n1 + n2));